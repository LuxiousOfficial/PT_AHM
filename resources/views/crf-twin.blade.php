                        <p>
                            Jakarta - PT Astra Honda Motor (AHM) menghadirkan model terbaru dari CRF1100L Africa 
                            Twin dengan desain dan dan performa terbaru untuk menjawab kebutuhan pecinta big bike 
                            adventure di Indonesia. Garis desain baru yang memberikan kesan lebih garang berciri khas 
                            tampilan motor adventure, dikombinasikan dengan racikan mesin baru yang lebih bertenaga, siap 
                            melengkapi gaya hidup petualang yang membanggakan. 
                            <br>
                            Model terbaru ini menggendong mesin anyar berkapasitas 1.084 cc 4 stroke SOHC dengan konfigurasi 
                            parallel twin-cylinder berpendingin cairan yang lebih bertenaga. CRF1100L Africa twin kini mampu 
                            menghasilkan tenaga lebih maksimal yaitu 75kW @7500 rpm dan torsi puncak lebih besar mencapai 
                            112 Nm @5.500 rpm. Kompresi mesin model ini juga meningkat menjadi 10,5:1 dengan karakter tenaga 
                            yang kuat di semua putaran mesin. Pengaplikasian 4+2 riding mode tetap dipertahankan, memberikan 
                            kebebasan bagi pengendara untuk memilih mode yang sesuai dengan jalanan yang dihadapi yaitu mode tour, 
                            urban, gravel dan off-road. Sementara itu, untuk 2 riding mode tambahan memberikan pilihan mode yang 
                            dapat diatur sendiri oleh pengendara dalam menyesuaikan kebutuhannya dalam berkendara. 
                            <br>
                            CRF1100L Africa Twin kini juga dilengkapi dengan fitur canggih terbaru di bagian suspensi yang 
                            menggunakan Showa Electronically Equipped Ride Adjustment (EERA) inverted front suspension. Suspensi 
                            terbaru ini memungkinkan pengendara untuk memilih pengaturan suspensi yang paling sesuai dengan 
                            keinginannya secara elektronik, sehingga memberikan kenyamanan dan kebanggan lebih saat mengendarainya. 
                            Suspensi ini dapat diatur dengan 4 pengaturan preload mulai dari berkendara sendiri, hingga berboncengan 
                            dengan bagasi. Fitur teranyar ini diduetkan dengan keseluruhan desain terbaru yang lebih ramping, 
                            memudahkan pengendara mengendalikan sepeda motor lebih baik di berbagai medan. 
                            <br>
                            Marketing Director AHM Octavianus Dwi mengatakan CRF1100L Africa Twin terbaru dihadirkan seiring 
                            meningkatnya preferensi konsumen akan sepeda motor yang dapat melengkapi gaya hidup berkendara, terutama 
                            bagi mereka yang menyukai petualangan sejati berkendara jarak jauh dengan melintasi kondisi jalan yang 
                            beragam.
                            <br>
                            “Kami menghadirkan CRF1100L Africa Twin dengan berbagai ubahan terkini baik dari segi mesin yang lebih
                            bertenaga hingga fitur suspensi terbaru untuk memastikan pengalaman berkendara yang luar biasa dan 
                            membanggakan bagi pecinta big bike adventure di Indonesia. Kami pastikan CRF1100L Africa Twin siap 
                            menjadi partner konsumen melibas tantangan perjalanan jauh di berbagai medan baik on-road ataupun 
                            offroad,” ujar Octa.
                            <br>
                            Tampilan depan CRF1100L Africa Twin terbaru memberikan kesan garang pada motor ini. Didukung dengan 
                            desain jok dan tangki bensin yang ramping berkapasitas 18,8 liter, membuat pengendara semakin tangkas 
                            dalam melintasi jalanan off-road. Kesan posisi berkendara dengan gaya rally dan off road pun hadir pada 
                            model ini.
                            <br>
                            Sasis CRF1100L Africa Twin mendukung tiga sektor utama, yakni performa saat off-road, kenyamanan saat turing, 
                            dan kelincahan ketika berkendara harian. Honda Selectable Torque Control (HSTC) sistem pada model ini hadir
                            menawarkan tujuh tahap kontrol (mode), dan tiga level Wheelie Control dan Cornering ABS yang memungkinkan 
                            pengendara mendapat sensasi berkendara sesuai yang diinginkan.
