                    <div class="paragraf2">
                        <h5>Ragam Fitur Canggih</h5>
                        <p>
                            Ketangguhan CRF1100L Africa Twin semakin diperkuat dengan fitur yang lengkap dan canggih di 
                            kelasnya. Africa Twin memberikan kemudahan mengendalikan bagi pengendara, dengan kehadiran 6 
                            poros Inertial Measurement Unit (IMU) yang berfungsi untuk memonitoring kondisi motor ketika 
                            melakukan pengereman, cornering, dan berakselerasi.
                            <br>
                            Adjustable windscreen dengan design yang nyaman dapat diatur melalui lima level pengaturan, 
                            sehingga mampu meningkatkan visibilitas pengendara dengan tetap memberikan perlindungan dari
                            angin ketika melakukan perjalanan jauh. Tampilan full digital panelmeter dengan fitur 
                            Android Auto dan juga Apple Car Play dengan koneksi bluetooh dihadirkan untuk mempermudah 
                            pengendara mengintegrasikan dengan sistem operasi smartphone ketika berkendara.
                            <br>
                            Fitur-fitur canggih pada motor sport adventure ini pun mendukung pengendara dalam melakukan 
                            turing jarak jauh. Full digital panel meter yang merupakan layar sentuh TFT dengan besar 6,5 inch 
                            menampilkan informasi yang lengkap seperti riding mode, temperatur udara, kecepatan, dan banyak 
                            informasi lain yang dapat diatur sendiri oleh pengendara. Tersematnya fitur cruise control pada 
                            model ini turut memberikan rasa nyaman bagi pengendara saat menikmati perjalanan.  
                            <br>
                            Fitur DCT pada model ini dilengkapi dengan 2 mode, yaitu Mode D menawarkan keseimbangan terbaik 
                            antara konsumsi bahan bakar dan kenyamanan berkendara, sementara mode S memberikan tambahan 
                            performa sport. Ada juga ”G switch” untuk torsi lebih bertenaga pada RPM rendah saat melewati 
                            medan off-road. Saat fitur ”G” ini dimatikan, pengiriman tenaga akan lebih lembut. CRF1100L 
                            Africa Twin telah dilengkapi fitur cornering detect. Fitur ini berfungsi menghasilkan perpindahan 
                            gigi lebih natural saat melewati belokan.
                            <br>
                            AHM memasarkan CRF1100L Africa Twin dengan warna Tricolor, kombinasi merah, putih, dan biru dengan 
                            harga Rp633.000.000 (on the road DKI Jakarta). Model ini dapat dijumpai di seluruh gerai Honda Big 
                            Wing yang tersebar di 9 kota di Indonesia.
                        </p>
