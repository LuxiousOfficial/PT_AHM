                        <p>
                            Mataram- Pebalap Astra Honda Racing Team (AHRT) berhasil meraih podium tertinggi 
                            di race kedua ajang balap Asia Road Racing Championship (ARRC) 2024 seri Mandalika. 
                            Kemenangan dipersembahkan oleh M. Adenanta Putra yang menjadi juara pertama diikuti 
                            Veda Ega Pratama sebagai juara kedua pada kelas Supersports (SS) 600. Kemenangan lainnya 
                            juga diraih pada kelas Asia Production (AP)250 melalui keberhasilan M. Kiandra Ramadhipa 
                            meraih podium kedua di Pertamina Mandalika International Circuit, Lombok, Nusa Tenggara 
                            Barat pada hari Minggu (28/07/2024). 
                            <br>
                            Sejak awal balapan, Adenanta dan Veda mampu mempertahankan posisinya di tiga besar. 
                            Persaingan merebut podium tertinggi saling salip dengan pebalap lainnya terjadi hingga akhir 
                            balapan. Dengan mengandalkan performa terbaik CBR600RR, pada lap akhir Adenanta berhasil 
                            melakukan overtake dan menjadi juara pertama pada race kedua ARRC putaran keempat, diikuti 
                            oleh Veda yang berhasil meraih podium kedua dan mengibarkan bendera Merah Putih di kelas SS600. 
                            Dengan kemenangan ini, Adenanta membuat sejarah baginya, untuk pertama kali memenangkan balapan 
                            di kelas SS600 dan AHRT menjadi tim balap Indonesia pertama yang berhasil mengantarkan dua 
                            pebalapnya meraih double podium pertama dan kedua.
                            <br>
                            Selain Adenanta dan Veda, pebalap muda AHRT lainnya juga berupaya menampilkan performa balap terbaik. 
                            Fadillah Arbi Aditama yang turun sebagai pebalap wild card pada putaran keempat ARRC 2024 ini berhasil 
                            finish pada posisi ke-4 dan Rheza Danica Ahrens finish pada posisi ke-5.
                            <br>
                            “Alhamdullillah, akhirnya target saya untuk bisa meraih kemenangan dapat terwujud. Usaha keras saya 
                            dan team Astra Honda membuahkan hasil melalui pertarungan yang sangat ketat. Kemenangan ini saya 
                            persembahkan untuk team Astra Honda dan masyarakat Indonesia,” ujar Adenanta.
                            <br>
                            “Balapan yang sangat seru. Beberapa kali saya harus bertarung dengan ketat dan sempat terdesak ke belakang. 
                            Tapi saya mencoba untuk terus menekan. Pada akhirnya, saya berhasil finish pada posisi kedua. Terima kasih 
                            untuk team dan semua pihak yang telah mendukung saya. Saya bisa menutup pekan yang menyenangkan,” ujar 
                            Veda.
                            <br>
                            Balapan ARRC digelar sebanyak dua kali, pada race pertama yang dilaksanakan di hari Sabtu (27/07/2024) 
                            di kelas SS600, Adenanta yang memulai jalannya balap dari posisi ke-3 mampu menyelesaikan race 1 dengan 
                            meraih podium kedua setelah bersaing ketat dengan pebalap Honda lainnya asal Malaysia. Tetapi disayangkan 
                            Adenanta mendapatkan penalti 3 detik dikarenakan melakukan overtake pada saat yellow flag. Hal tersebut 
                            menyebabkan posisi Adenanta turun menjadi urutan ke-4. Sedangkan Veda yang memulai balap pada Pole 
                            Position berhasil finish pada posisi ke-4 menjadi meraih podium ketiga pada race pertama seri keempat 
                            ARRC 2024.
                            <br>
                        </p>
