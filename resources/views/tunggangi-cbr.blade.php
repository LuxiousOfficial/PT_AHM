                        <h5>Podium AP250</h5>
                        <p>
                            Pada kelas lainnya yaitu AP250, AHRT menurunkan dua pebalap yakni M. Kiandra 
                            Ramadhipa dan Herjun Atna Firdaus. Bersama CBR250RR, pada race kedua Ramadhipa menunjukkan 
                            performa pertarungan balap yang kompetitif untuk merebut podium tertinggi. Memulai jalannya 
                            balap dari grid kedua pada urutan keenam, Ramadhipa berupaya untuk terus berada di grup terdepan 
                            sejak start. Sempat memimpin rombongan, mendekati final lap Ramadhipa akhirnya berhasil meraih 
                            podium kedua dengan selisih yang sangat tipis dengan juara pertama.
                            <br>
                            Pebalap lainnya yaitu Herjun yang memulai balap dari urutan ke-8 juga berupaya memasuki rombongan 
                            terdepan. Sayang terjadi kesalahan pada saat balapan berlangsung yang membuat jaraknya menjadi 
                            semakin lebar, hingga akhirnya pebalap kelahiran Pati, Jawa Tengah tersebut harus puas untuk 
                            menyelesaikan race kedua kelas AP250 pada posisi ke 8.
                            <br>
                            Sedangkan pada seri pertama sehari sebelumnya, Herjun Atna Firdaus dan M. Kiandra Ramadhipa 
                            yang turun di kelas Asia Production (AP)250 nyaris meraih podium. Menghadapi tantangan baru dengan 
                            adanya pengurangan 200 Rpm pada putaran ini, kedua pebalap bertalenta tetap tampil kompetitif. 
                            Ramadhipa memulai balap dari urutan ke-6. Tetap berupaya tampil optimis, Ramadhipa harus puas 
                            menyelesaikan jalannya balap di urutan ke 6, diikuti Herjun di posisi ke-7.
                            <br>
                            “Alhamdullillah, saya bisa meraih podium 2 pada race 2. Belajar dari race pertama kemarin, 
                            saya mencoba mengubah strategi untuk bisa lebih tenang dan bertahap masuk ke group depan. Strategi 
                            saya berhasil, dalam beberapa lap saya bisa memimpin balapan. Sayang pertarungan sangat rapat di 
                            lap akhir. Posisi pertama tidak bisa saya pertahankan. Bagaimanapun juga ini hasil terbaik yang bisa 
                            saya raih. Terima kasih team yang sudah bekerja keras memberikan setting terbaik untuk CBR250RR saya. 
                            Terima kasih untuk semua pihak yang sudah mendukung saya,” ujar Ramadhipa.
                            <br>
                            General Manager Marketing Planning & Analysis Division AHM Andy Wijaya mengatakan kemenangan ini 
                            merupakan hasil dari upaya para pebalap dan mekanik untuk terus menampilkan yang terbaik bagi masyarakat. 
                            Dukungan tanpa henti yang diberikan masyarakat Indonesia terhadap para pebalap AHRT tentu menjadi energi 
                            tersendiri bagi para pebalap saat bersaing di negeri sendiri membawa nama negara Indonesia di pentas balap 
                            bergengsi, khususnya di level Asia.
                            <br>
                            “Balapan akhir pekan kali ini sangat menegangkan dan juga membanggakan bagi Indonesia. Tentunya hasil 
                            positif yang didapat para pebalap AHRT bisa menjadi modal semangat untuk terus mencetak berbagai prestasi 
                            gemilang di putaran-putaran selanjutnya. Kami akan terus mendampingi dan memberikan dukungan tanpa henti 
                            kepada talenta terbaik di negeri ini untuk mencapai mimpi tertinggi di dunia balap, ” ujar Andy.
                            <br>
                            Persaingan para pebalap terbaik Asia ini akan berlanjut pada 13-15 September 2024 di putaran kelima 
                            yang digelar di Petronas Sepang International Circuit, Malaysia.
                        </p>
