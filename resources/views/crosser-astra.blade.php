                        <p>
                            Mataram - Crosser binaan PT Astra Honda Motor (AHM) Delvintor Alfarizi berhasil meraih poin 
                            perdananya musim ini pada kejuaraan dunia FIM Motocross World Championship (MXGP) kelas MX2, 
                            di Sirkuit Selaparang, Lombok, Nusa Tenggara Barat, Minggu, 30 Juni 2024. Delvintor memberikan 
                            penampilan terbaik salam persainganya dengan para pebalap dunia lainnya. 
                            <br>
                            Turun sebagai pebalap reguler bersama JM Racing Astra Honda, Adel sapaan akrab Delvintor, 
                            berusaha terus meningkatkan performa balapnya sejak sesi free practice, time practice, dan 
                            qualifying race pada Sabtu, 29 Juni 2024. Adel memulai jalannya balapan pada posisi ke-18 
                            dan bersama tim berupaya menentukan strategi saat race day. Cuaca panas dan sedikit berangin 
                            di Selaparang memberikan tantangan tersendiri bagi para crosser. 
                            <br>
                            Pada race pertama, Adel langsung tancap gas untuk masuk ke persaingan grup tengah. Dirinya 
                            sempat kehilangan waktu cukup banyak pada lap ke-9 saat memasuki pit untuk memperbaiki rem belakang. 
                            Setelahnya, crosser kelahiran Jakarta ini berusaha mengejar ketertinggalan dan mengerahkan kemampuan 
                            terbaiknya. Perjuangan Adel dalam 30 menit +2 lap ditutup dengan finish di posisi ke-19 dan mengemas 
                            2 poin.
                            <br>
                            Race kedua Adel tampil jauh lebih baik. Meski lintasan semakin becek akibat penyiraman ekstra, namun 
                            dirinya mampu melaju cepat pada lap pertama menyusul beberapa crosser. Dirinya pun mengakhiri race 
                            kedua di posisi 15 dan mengemas 6 poin tambahan.
                            <br>
                            ”Pada race pertama, di 10-12 menit, rem belakang mulai dalam dan hampir hilang (cengkeraman). Setelah 
                            diperbaiki, saya coba bertahan dan roll sampai finish. Lalu di race kedua, setelah mendapatkan posisi 
                            bagus, saya fight sampai akhir. Hasil yang cukup bagus, semoga di race berikutnya bisa lebih baik lagi,” 
                            kata Adel.
                            <br>
                            Sementara Arsenio Algifari yang turun sebagai wildcard bersama Astra Honda Racing Team, juga tampil cukup 
                            baik. Catatan waktunya cukup konsisten dari lap ke lap dan mampu finish satu peringkat di bawah Adel pada 
                            race pertama. Lalu di race kedua, Arsenio mampu finish ke-18. Seharusnya, Arsenio mengantongi total 4 poin 
                            dari dua race, namun poin tersebut harus dia relakan karena kesalahan melanggar bendera kuning.
                            <br>
                            ”Pada race pertama, saya mengawalinya sesuai rencana untuk mempertahankan kecepatan secara 
                            konstan. Sayang di lap ketiga, saya kehilangan kendali dan jatuh. Namun saya tampil lebih fight pada 
                            race kedua dan mendapat hasil cukup baik. Saya lalai melihat bendera kuning dikibarkan, dan ini 
                            menjadi pengalaman dan pelajaran yang sangat berarti,” ucap Arsenio.
                            <br>
                            General Manager Marketing Planning and Analysis AHM Andy Wijaya mengatakan raihan para crosser binaan 
                            akan menjadi penyemangat pada seri-seri selanjutnya. Tentunya, dukungan tanpa henti akan terus diberikan 
                            perusahaan kepada talenta crosser Tanah Air.
                            <br>
                            ”Seri MXGP Lombok menjadi bekal kuat bagi tim dan crosser kami untuk bersaing lebih kompetitif di balapan 
                            berikutnya. Perjuangan bersaing dengan crosser-crosser dunia tentu menjadi bekal berharga bagi Adel dan 
                            Arsenio untuk terus menantang dirinya. Kami ingin terus menemani crosser muda berbakat Tanah Air dalam 
                            menggapai mimpi tertingginya,” ujar Andy.
                            <br>
                            MXGP seri Indonesia masih menyisakan putaran Lombok kedua yang akan diselenggarakan di lokasi 
                            yang sama, sirkuit Selaparang, Mataram, Lombok pada 6-7 Juli 2024. Komunitas Honda pun akan 
                            turut memeriahkan gelaran MXGP tersebut.
                        </p>
