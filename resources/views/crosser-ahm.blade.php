                            berupaya tampil lebih baik di race kedua dan berhasil finish di urutan ke 17 sekaligus menambah 
                            pundi-pundi poinnya dengan total raihan 12 poin di musim ini.
                            <br>
                            "Saya start dengan baik saat di race pertama, namun saya mengalami kendala teknis di lap pertama. 
                            Kita mencoba mengatasinya di pit, namun tidak memungkinkan. Pada race kedua, saya merasa lebih 
                            konsisten secara kecepatan dibanding saat sesi kualifikasi. Ini bukan hari yang terbaik, namun saya 
                            tetap bersyukur. Sekarang saya akan fokus istirahat dan mempersiapkan diri putaran final di Eropa," 
                            ujar Adel.
                            <br>
                            Crosser Indonesia binaan AHM lainnya, Arsenio Algifari juga telah berupaya tampil maksimal pada akhir 
                            pekan ini. Menjadi wild card untuk kedua kalinya bersama Astra Honda Racing Team di kelas MX2, Arsenio 
                            mendapatkan banyak pengalaman berharga. Pada race pertama Arsenio mampu tampil konsisten dan berhasil 
                            meraih 3 poin pertamanya sebagai wild card dengan finish di posisi ke 18. Sedangkan pada race kedua 
                            Arsenio harus menelan pil pahit setelah terjatuh dan tidak dapat meneruskan balapan.
                            <br>
                            "Saya mencoba tampil maksimal di race 2 ini. Berbagai koreksi dari penampilan sebelumnya saya sudah pelajari. 
                            Namun sayang, saya harus terhenti karena terjatuh dan tidak dapat menyelesaikan balapan. Yang pasti, 
                            apapun hasil yang dalam balapan, bagi saya bisa tampil di kejuaraan dunia seperti ini sangat berarti. 
                            Terima kasih Astra Honda atas kesempatannya. Terima kasih juga untuk semua pihak yang telah mendukung saya,
                            " ujar Arsenio.
                            <br>
                            General Manager Marketing Planning and Analysis PT Astra Honda Motor (AHM) Andy Wijaya mengatakan upaya
                            dan konsistensi dua crosser muda Indonesia yang terus menunjukan performa terbaiknya patut diberikan 
                            apresiasi. AHM akan terus mendukung para crosser bertalenta Tanah Air dalam mewujudkan mimpi mereka 
                            dalam upaya mengharumkan nama bangsa Indonesia di kancah dunia.
                            <br>
                            "Sejalan dengan Semangat Satu Hati, kami akan selalu mendukung crosser Tanah Air dalam mewujudkan 
                            mimpinya untuk menjadi yang terbaik dan mampu bersaing dengan crosser lainnya di kejuaraan kelas dunia," 
                            ujar Andy.
