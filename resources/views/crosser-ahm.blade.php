                            Motorcross World Championship (MXGP) kelas MX2 seri Indonesia yang digelar Minggu, 7 Juli 2024 
                            di Sirkuit Selaparang, Lombok, Nusa Tenggara Barat. 
                            <br>
                            Tampil dengan catatan waktu yang konsisten Arsenio berhasil meraih poin pertamanya pada race 
